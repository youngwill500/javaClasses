package reviewClass;

public class PrintTheWhole2DArray {

	public static void main(String[] args) {
		
		
		  int[][] b = { 
				  { 2, 3, 5 }, 
				  { 2, 4, 7 },
				  { 8, 1, 9 }, 
				  { 3, 5, 1 } 
				  };
		  
	//This code prints out the entire 2D Array	  
		  
	for(int i = 0; i < b.length; i++) {
		for(int j = 0; j < b[i].length; j++) {
				
				System.out.print(b[i][j]+" ");			
		}
		System.out.println();
	}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
