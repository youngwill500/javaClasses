package com.class9;

public class NestedLoopsTask1 {
	public static void main(String[] args) {
	
		for (int i=1; i<=9; i++) {
			for (int j=1; j<=i; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		for (int h=9; h>=1; h--) {
			for (int c=1; c<=h; c++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
	}

}
