package com.class9;

public class One_Through_9 {

	public static void main(String[] args) {
		
		for (int i=1; i<=9; i++) {
			for (int j=1; j<=i; j++) {
				System.out.print(i);
			}
			System.out.println();
		}
		
		
		System.out.println("*******************");
		
		for (int a=9; a>=0; a--) {
			for (int b=9; b>=a; b--) {
				System.out.print(a);
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
