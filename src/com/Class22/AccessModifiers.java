package com.Class22;

public class AccessModifiers {

	
		
		// 1. Access modifiers -> applicable for variables, methods, constructor or class
		
		/* Types of access modifiers:
		 * Private - Most restrictive
		 * Default
		 * Protected
		 * Public - making variables and method be accessible in the entire project
		 */
		
		
		/*
		 * SYNTAX for Variables
		 * 	optional	MUST		MUST
		 *   access modifier static dataType variableName;
		 * 
		 * 
		 */
		
		
		public static void hello() {
			System.out.println("hello");
		}
		
		protected static void hello1() {
			System.out.println("hello");
		}
		
		 static void hello2() {
			System.out.println("hello");
		}

	}
